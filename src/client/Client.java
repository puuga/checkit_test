package client;

public class Client {
	private String mac;
	private String senderTime;
	private String senderFormatedTime;

	public String getMac() {
		return mac;
	}

	public void setMac(String mac) {
		this.mac = mac;
	}

	public String getSenderTime() {
		return senderTime;
	}

	public void setSenderTime(String senderTime) {
		this.senderTime = senderTime;
	}

	public String getSenderFormatedTime() {
		return senderFormatedTime;
	}

	public void setSenderFormatedTime(String senderFormatedTime) {
		this.senderFormatedTime = senderFormatedTime;
	}
}
